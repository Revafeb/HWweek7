// Rumus Luas dan Keliling Persegi
export function square (side) {
    return side * side;
}

export function squarePerimeter (side) {
    return 4 * side;
}

// Rumus Luas dan Keliling Persegi Panjang
export function rectangle (length, width) {
    return length * width;
}

export function rectanglePerimeter (length, width) {
    return 2 * (length + width)
} 